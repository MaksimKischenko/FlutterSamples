class ClubInfoMatch {
  final String firstClubImagePath;
  final String firstClubName;
  final String secondClubImagePath;
  final String secondClubName;
  final int firstScoreNum;
  final int secondScoreNum;

  ClubInfoMatch({
    required this.firstClubImagePath,
    required this.firstClubName,
    required this.secondClubImagePath,
    required this.secondClubName,
    required this.firstScoreNum,
    required this.secondScoreNum,
  });
}
